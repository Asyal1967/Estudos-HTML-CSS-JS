// Number 
console.log(typeof 2);
console.log(typeof 2.4);
console.log(typeof -225);

// Operações aritméticas
console.log(2+2);
console.log(12*112);
console.log(50-25);
console.log(42/4);

// Special numbers
console.log(typeof Infinity);
console.log(typeof -Infinity);
console.log(typeof NaN);
console.log(typeof 1 * 'oi');

// Strings
console.log('pulando \n linha');
console.log('espaçamento com \t Tab')

// Concatenação
console.log('usando ' + 'concatenção')

// Interpolação(Template strings)
console.log(`a soma de 5 + 5 é ${5 + 5}`);

// Booleano
console.log(5 < 2);
console.log(5 > 2);

// Comparações
console.log(5 >= 6);
console.log(5 < 6);
console.log(5 == 6);
console.log(5 === 5);
console.log(10 != 9);

// Comparações de identico
console.log(7 == '7');
console.log(7 === '7');
console.log(7 != '7');
console.log(7 !== '7');